// ----------------------------------------------------------------------------------------------------------------------------------- //
import { Injectable } from '@angular/core';
import { LOCALE_ID, Inject } from '@angular/core';
// ----------------------------------------------------------------------------------------------------------------------------------- //
@Injectable({
  providedIn: 'root',
})
export class GlobalsService {
 dir: string;
 private _err: Error;
  setDirection() {
    if (this.localeId === 'ar-SA' || this.localeId === 'ar') {
      this.dir = 'rtl';
    } else {
      this.dir = 'ltr';
    }
  }
  // ----------------------------------------------------------------------------------------------------------------------------------- //
  constructor(@Inject(LOCALE_ID) public localeId: string) {
    this.setDirection();
  }
  // ----------------------------------------------------------------------------------------------------------------------------------- //
  get UILanguage(): string {
    return this.localeId;
  }
  // ----------------------------------------------------------------------------------------------------------------------------------- //
  public EncryptValue(val: string): string {
    return '';
  }
  // ----------------------------------------------------------------------------------------------------------------------------------- //
  set ErrorRaised(err: Error) {
    this._err = err;
  }
  get ErrorRaised(): Error {
    return this._err;
  }
  // ----------------------------------------------------------------------------------------------------------------------------------- //
}